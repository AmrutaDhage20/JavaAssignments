//Program for Primitive Datatype
package com.encora.javaAssignments;
public class PrimitiveDataTypes {
	public static void main(String[] args) {
		// declaration of primitive data types
		char singleChar='A';
		byte time=2;
		short age=22;
		int number=45;
		float firstFloat=5.12345f;
		double speed=4.6829d;
		boolean result=true;
		System.out.println("char:"+singleChar);//Prints the char data
		System.out.println("byte:"+time);//Prints the byte data
		System.out.println("short:"+age);//Prints the short data
		System.out.println("int:"+number);//Prints the int data
		System.out.println("float:"+firstFloat);//Prints the float data
		System.out.println("double:"+speed);//Prints the double data
		System.out.println("boolean:"+result);//Prints the boolean data
	}
}