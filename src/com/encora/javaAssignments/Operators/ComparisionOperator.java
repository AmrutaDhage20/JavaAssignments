package Operators;

public class ComparisionOperator {
    public static void main (String[] args){
        int p = 1;
        int q = 2;

        if (p==q){
            System.out.println("p==q");
        }
        if (p!=q){
            System.out.println("p!=q");
        }
        if (p>q){
            System.out.println("p>q");
        }
        if (p<q){
            System.out.println("p<q");
        }
        if (p<=q){
            System.out.println("p<=q");
        }
    }
}