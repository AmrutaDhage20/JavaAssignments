package Operators;

public class ArithmeticOperator {
    public static void main(String[] args){
        int num1 = 2000;
        int num2 = 500;

        System.out.println("Addition of two numbers is:" +(num1+num2)); //print the addition of 2000+500
        System.out.println("Subtraction of two numbers is:" +(num1-num2)); // print the subtraction of 2000 - 500
        System.out.println("Multiplication of two numbers is:" +(num1*num2));//prints the multiplication of 2000*500
        System.out.println("Division of two numbers is:" +(num1/num2));//prints the division of 2000/500
        System.out.println("Modulus of two numbers is:" +(num1%num2));//prints the modulus of 2000%500
    }
}
