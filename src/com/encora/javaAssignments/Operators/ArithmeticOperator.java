//Addition, subtraction, multiplication, division, modulus of two integers
package Operators;
public class ArithmeticOperator {

    public static void main(String[] args){

        int speed = 2000;
        int weight = 500;

        System.out.println("Addition of two numbers is:" +(speed+weight)); //print the addition of 2000+500
        System.out.println("Subtraction of two numbers is:" +(speed-weight)); // print the subtraction of 2000 - 500
        System.out.println("Multiplication of two numbers is:" +(speed*weight));//prints the multiplication of 2000*500
        System.out.println("Division of two numbers is:" +(speed/weight));//prints the division of 2000/500
        System.out.println("Modulus of two numbers is:" +(speed%weight));//prints the modulus of 2000%500
    }
}
