package Operators;

public class PrePostDemo {
    public static void main(String[] args){
        int i=3;
        i++;
        System.out.println(i);//print 4
        ++i;
        System.out.println(i);//print 5
        System.out.println(++i);//print 6
        System.out.println(i++);//print 6
        System.out.println(i);//print 7

    }
}
